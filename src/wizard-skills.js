export const WIZARD_SKILL_LIBRARY = [
  ...FIRE_SKILLS, ...LEAF_SKILLS, ...WATER_SKILLS,
  ...LIGHT_SKILLS, ...DARK_SKILLS, ...SPARK_SKILLS,
];
_autoAssignSelfBuffs(WIZARD_SKILL_LIBRARY);

// 指定タイプ + プレイヤーレベルで「習得済みであるべき技」を列挙
export function wizardSkillsLearnableAt(element, level) {
  const list = WIZARD_SKILLS_BY_ELEMENT[element] ?? [];
  return list.filter(sk => sk.learnedAt <= level);
}

export function findWizardSkillById(id) {
  return WIZARD_SKILL_LIBRARY.find(s => s.id === id) ?? null;
}
