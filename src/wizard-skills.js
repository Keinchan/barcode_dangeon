// ─────────────────────────────────────────────
// 主人公（魔法使い）のタイプ別技習得表
// ─────────────────────────────────────────────
//   設計書 wizard_moves_with_range.md 準拠。
//   6 タイプ（火 / 水 / 草 / 光 / 闇 / 雷） × Lv1〜100 の自動習得技。
//   レベルアップ時、自分のタイプの primary 属性に対応する技を自動で覚える。
//
//   - つえふり: 全タイプ共通の Lv1 基本技（属性は持つが汎用）
//   - 各タイプ固有 Lv1 まなざし系 + Lv4〜100 までの 25 レベル毎の技
//   - SELF 技（ルーンよみ・チャージ等）は support 扱いでダメージを与えない
//   - 究極技（Lv77 / Lv88 / Lv100）はレジェンド扱い + 高威力 + 高 MP コスト
//
//   設計書の PP は MP コストに換算（PP 大 = 軽い、PP 小 = 重い）:
//     PP 30→MP 4, 25→5, 20→7, 15→11, 10→16, 5→28, 3→38, 1→60

// 技レベルからレアリティ算出（UI の色付け / 既存スキル枠との整合）
function _rarityForLevel(lv) {
  if (lv >= 73) return 'レジェンド';
  if (lv >= 45) return 'エピック';
  if (lv >= 16) return 'レア';
  return 'コモン';
}

// 技レベルから dmgMult 算出（攻撃技の威力カーブ）
function _dmgMultForLevel(lv) {
  if (lv === 100) return 6.0;
  if (lv === 88)  return 4.5;
  if (lv === 77)  return 5.0;
  if (lv >= 60)   return 3.0;
  if (lv >= 45)   return 2.5;
  if (lv >= 32)   return 2.0;
  if (lv >= 23)   return 1.7;
  if (lv >= 16)   return 1.4;
  if (lv >= 10)   return 1.2;
  if (lv >= 4)    return 1.0;
  return 0.8;
}

// 設計書の PP 値を MP コストに換算
function _ppToMp(pp) {
  switch (pp) {
    case 30: return 4;
    case 25: return 5;
    case 20: return 7;
    case 15: return 11;
    case 10: return 16;
    case 5:  return 28;
    case 3:  return 38;
    case 1:  return 60;
    default: return Math.max(4, Math.round(80 / Math.max(1, pp)));
  }
}

// SELF 範囲は基本的に support。FLOOR / FLOOR_ALL は攻撃 + フィールド変化を兼ねるが
// ダメージ計算は通常の dmgMult で行う。
function _isSupportRange(range) {
  return range === 'SELF';
}

// 技 1 件を組み立て。id は `wiz_<elementSlug>_<lv>[_<idx>]` 形式で決定論的。
const _ELEMENT_SLUG = { '火': 'fire', '水': 'water', '草': 'leaf', '光': 'light', '闇': 'dark', '雷': 'spark' };

function W(element, lv, name, range, pp, desc, opts = {}) {
  const slug = _ELEMENT_SLUG[element] ?? 'gen';
  const idx  = opts.idx ?? 0;
  const id   = idx === 0 ? `wiz_${slug}_${lv}` : `wiz_${slug}_${lv}_${idx}`;
  const support = _isSupportRange(range) || opts.support === true;
  const skill = {
    id,
    name,
    pattern:  range,                          // 新範囲タイプ ID（CROSS / LINE3 / SELF / ROOM 等）
    dmgMult:  support ? 0 : (opts.dmgMult ?? _dmgMultForLevel(lv)),
    mpCost:   opts.mpCost  ?? _ppToMp(pp),
    element,
    rarity:   opts.rarity  ?? _rarityForLevel(lv),
    desc,
    learnedAt: lv,                            // タイプ自動習得時のレベル閾値
    isWizard: true,                           // ライブラリ判別用
    support,                                  // ダメージ無し技の判定
    pp,                                       // 設計書の PP 値（情報用）
  };
  if (opts.status)    skill.status    = opts.status;
  if (opts.knockback) skill.knockback = opts.knockback;
  if (opts.selfBuff)  skill.selfBuff  = opts.selfBuff;
  return skill;
}

// SELF 技の自己バフ自動アサイン。学習レベルでバフ強度を決める。
//   Lv7  ルーンよみ系     → 攻撃力アップ（atkUp,    6T）
//   Lv13 まりょくチャージ → 攻撃力アップ・強（atkUpHigh, 3T）
//   Lv20 シールド/ベール  → 防御力アップ（defUp,    5T）
//   Lv35 高位シールド     → 防御力アップ・強（defUpHigh, 3T）
//   Lv43 ステップ系       → 瞬発力アップ（agility,   3T）— 1 ターン 2 回行動
//   Lv66 タイムディレイ系 → 瞬発力アップ・強（agilityHigh, 2T）— 1 ターン 3 回行動
// 既存スキル定義に手作業で書き足すのは 36 行になるので、ID パターンで一括付与する。
function _autoAssignSelfBuffs(list) {
  for (const sk of list) {
    if (sk.pattern !== 'SELF') continue;
    if (sk.selfBuff) continue;        // 既に手動指定があればそれを尊重
    const m = (sk.id || '').match(/^wiz_\w+_(\d+)$/);
    if (!m) continue;
    const lv = parseInt(m[1], 10);
    if      (lv === 7)  sk.selfBuff = { kind: 'atkUp',       turns: 6 };
    else if (lv === 13) sk.selfBuff = { kind: 'atkUpHigh',   turns: 3 };
    else if (lv === 20) sk.selfBuff = { kind: 'defUp',       turns: 5 };
    else if (lv === 35) sk.selfBuff = { kind: 'defUpHigh',   turns: 3 };
    else if (lv === 43) sk.selfBuff = { kind: 'agility',     turns: 3 };
    else if (lv === 66) sk.selfBuff = { kind: 'agilityHigh', turns: 2 };
  }
}

